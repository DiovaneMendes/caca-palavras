
public class Enunciado {
	/*
	 Objetivo

		Implementar um algoritmo que crie um tabuleiro de jogo de "Caça Palavras".
		=========================================================================
		
		Tarefas
		
		Para a obtenção do conceito C, são necessárias as seguintes funcionalidades:
			Ler a partir de um arquivo uma lista de palavras, uma por linha.
			Gerar um grid com as palavras carregadas, na horizontal, da esquerda para a direita, ou na vertical, de cima para baixo.
			Nas posições em que não se encontram letras das palavras, inserir letras aleatórias.
			Fazer com que existam cruzamentos entre as palavras. Pelo menos 3 cruzamentos simples, onde uma palavra cruza uma outra, 
			e um cruzamento duplo, onde uma palavra cruza duas outas. As palavras não podem estar em linhas ou colunas adjacentes no grid.
		------------------------------------------------------------------------------------------------------------------------------------------------
		Para a obtenção do conceito B, é necessário cumprir todos os requisitos para a obtenção do conceito C, além de, 
		ao menos uma das seguintes características extras e/ou modificações:
		
			As palavras podem estar na vertical de cima para baixo, ou de baixo para cima; e na horizontal, da esquerda para a direita, 
			ou da direita para a esquerda..
			As palavras podem estar da diagonal, de cima para baixo, ou de baixo para cima, da direita para a esquerda.
		------------------------------------------------------------------------------------------------------------------------------------------------
		
		Para a obtenção do conceito A, e necessário cumprir todos os requisitos para a obtenção do conceito C, todos os requisitos para a 
		obtenção do conceito B, além de pelo menos uma das seguintes modificações:
			As palavras podem estar na horizontal, vertical, ou diagonal, em qualquer direção.
			As palavras podem estar em linhas ou colunas adjacentes, desde que seu início e/ou fim, coincidam.
		------------------------------------------------------------------------------------------------------------------------------------------------
		
		NOTA: Em qualquer situação, as palavras não podem se "sobrescrever" (ex.: macacoelho), ou começar exatamente quando outra termina (ex.: bolachute).
	 */
}
